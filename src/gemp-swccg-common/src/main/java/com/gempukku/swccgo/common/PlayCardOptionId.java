package com.gempukku.swccgo.common;

/**
 * Used by cards to identify each play card option. This is needed for cards (usually Effects) that can be
 * played for multiple reasons.
 */
public enum PlayCardOptionId {
    PLAY_CARD_OPTION_1,
    PLAY_CARD_OPTION_2,
    PLAY_CARD_OPTION_3,

    PLAY_AS_DEJARIK,
    PLAY_AS_INSERT_CARD
}
