/**
 * Set: Set 9
 * Type: Character
 * Subtype: Rebel
 * Title: Saw Gerrera
 */
public class Card209_012 extends AbstractRebel {
    public Card209_012() {
        super(Side.LIGHT, 2, 4, 4, 3, 6, "Saw Gerrera", Uniqueness.UNIQUE, ExpansionSet.SET_9, Rarity.V);
        setLore("Leader.");
        setGameText("Attrition against opponent is +1 here for each of their characters present. Opponent may not reduce your Force drains here. Cancels Trooper Assault here.");
        addIcons(Icon.WARRIOR, Icon.VIRTUAL_SET_9);
        addKeywords(Keyword.LEADER);
    }

    @Override
    protected List<Modifier> getGameTextWhileActiveInPlayModifiers(SwccgGame game, final PhysicalCard self) {
        String playerId = self.getOwner();
        String opponent = game.getOpponent(playerId);

        List<Modifier> modifiers = new LinkedList<Modifier>();
        modifiers.add(new AttritionModifier(self, Filters.here(self), new PresentEvaluator(self, Filters.and(Filters.opponents(self), Filters.character)), opponent));
        modifiers.add(new ForceDrainsMayNotBeReducedModifier(self, Filters.here(self), opponent, playerId));
        return modifiers;
    }

    @Override
    protected List<RequiredGameTextTriggerAction> getGameTextRequiredBeforeTriggers(final SwccgGame game, Effect effect, final PhysicalCard self, int gameTextSourceCardId) {
        // Check condition(s)
        if (TriggerConditions.isPlayingCard(game, effect, Filters.Trooper_Assault)
                && GameConditions.canCancelCardBeingPlayed(game, self, effect)
                && GameConditions.isDuringBattleAt(game, Filters.here(self))) {

            RequiredGameTextTriggerAction action = new RequiredGameTextTriggerAction(self, gameTextSourceCardId);
            // Build action using common utility
            CancelCardActionBuilder.buildCancelCardBeingPlayedAction(action, effect);
            return Collections.singletonList(action);
        }/**/
        return null;
    }

    @Override
    protected List<RequiredGameTextTriggerAction> getGameTextRequiredAfterTriggers(SwccgGame game, final EffectResult effectResult, final PhysicalCard self, int gameTextSourceCardId) {
        List<RequiredGameTextTriggerAction> actions = new LinkedList<RequiredGameTextTriggerAction>();

        // Check condition(s)
        if (TriggerConditions.isTableChanged(game, effectResult)
                && GameConditions.canTargetToCancel(game, self, Filters.Trooper_Assault)
                && GameConditions.isDuringBattleAt(game, Filters.here(self))) {


            final RequiredGameTextTriggerAction action = new RequiredGameTextTriggerAction(self, gameTextSourceCardId);
            // Build action using common utility
            CancelCardActionBuilder.buildCancelCardAction(action, Filters.Trooper_Assault, Title.Trooper_Assault);
            actions.add(action);

        }
        return actions;
    }
}
